import { API_URL } from "../utils/constants";

export async function getReubications(auth) {
  try {
    const url = `${API_URL}/api/reubicaciones?populate=user&populate=formato_entrada`;
    const params = {
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${auth.token}`,
      },
    };
    const response = await fetch(url, params);
    const result = await response.json();
    return result;
  } catch (error) {
    console.log(error);
    return null;
  }
}

export async function newOneReubication(auth, data) {
  try {
    const url = `${API_URL}/api/reubicaciones`;
    const params = {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${auth.token}`,
      },
      body: JSON.stringify({ data: { ...data } }),
    };
    const response = await fetch(url, params);
    const result = await response.json();
    return result;
  } catch (error) {
    console.log(error);
    return null;
  }
}

